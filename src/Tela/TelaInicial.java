    private void botao_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_salvarActionPerformed
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
      

        DadosCadastro cadastro = new DadosCadastro();

        DateTimeFormatter formatterHora = DateTimeFormatter.ofPattern("HH:mm");
        
        /*VALIDAÇÕES------------------*/
        
        if (pressao1_text.getText().contains(",") && pressao1_text.getText().contains(".") && pressao2_text.getText().contains(",") && pressao2_text.getText().contains(".") && hora_text.getText().contains(".") && data_text.getText().contains(".")){
            JOptionPane.showMessageDialog(null,"O valor deve ser escrito sem vírgula nem ponto");
            return;
       }
        
        if (data_text.getText().isEmpty() && hora_text.getText().isEmpty() && pressao1_text.getText().isEmpty() && pressao2_text.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            return;  
   
        } else {                  
            String txtdata = data_text.getText(); 
            boolean palavra = txtdata.matches("[0-9]{2}[/][0-9]{2}[/][0-9]{4}");
            String txthora = hora_text.getText(); 
            boolean palavra2 = txthora.matches("[0-9]{2}[:][0-9]{2}");
            
                //DATA ERRADA
                if(palavra==false && palavra2==true){
                        JOptionPane.showMessageDialog(null, "Data precisa ser no formato DD/MM/AAAA. Exemplo: 10/10/2010 "); 
                        return;
                    }
                    //HORA ERRADA
                    else if(palavra==true && palavra2==false){
                        JOptionPane.showMessageDialog(null, "Hora precisa se no formato  HH:MM. Exemplo: 10:30"); 
                        return;
                    }
                        
                }
        
        //-------------------------------
   
        String data_paciente = data_text.getText();
        try {        
            Date data = sdf.parse(data_text.getText());
            cadastro.setData(data);
               

            LocalTime hora = LocalTime.parse(hora_text.getText(), formatterHora);

            cadastro.setHora(hora);
            cadastro.setPressao1(Integer.parseInt(pressao1_text.getText()));
            cadastro.setPressao2((Integer.parseInt(pressao1_text.getText())));
            String falseTrue = "";
        if (situacao_estresse.isSelected()){
           falseTrue = "Sim"; 
           cadastro.setSituacao_estresse(falseTrue);

        } else {
            falseTrue = "Não";
            cadastro.setSituacao_estresse(falseTrue);
        }

        ListaDados.adicionar(cadastro);
        
        AtualizarTabela();
        
        } catch (ParseException ex) {
            Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_botao_salvarActionPerformed

