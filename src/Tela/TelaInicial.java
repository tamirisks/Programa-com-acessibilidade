    public void AtualizarTabela(){
        
        
        String[] colunas = { "Data", "Hora", "Pressão Sistólica", "Pressão Diastólica", "Situação de estresse"};
        
        DefaultTableModel tableModel = new DefaultTableModel(colunas, 0);
        
        List<DadosCadastro> listaCompleta = ListaDados.getLista_dados();
                
        //Convertendo
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("HH:mm");
        Date hora = Calendar.getInstance().getTime();
        String dataFormatada = sdf.format(hora);
    
        for (DadosCadastro c : listaCompleta) {
            String[] linha = {
                sdf.format(c.getData()),
                dtf.format(c.getHora()),
                String.valueOf(c.getPressao1()),
                String.valueOf(c.getPressao2()),
                c.getSituacao_estresse()
            };
            tableModel.addRow(linha);
        }      
            
            
        tabela_dados.setModel(tableModel);
    }
    
    

